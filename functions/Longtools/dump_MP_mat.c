/**************************************************************************\
@---------------------------------------------------------------------------
@ void *dump_MP_mat(Mat,rows,cols,comment)
@ MP_int **Mat;
@ int rows,
@     cols;
@ char *comment;
@
@ dumps an array of MP_int to stdout
@ 
@---------------------------------------------------------------------------
@
\**************************************************************************/
void 
dump_MP_mat (MP_INT **Mat, int rows, int cols, char *comment)

{

   int i,j;

   printf("%dx%d %% %s\n",rows,cols,comment);
   for (i=0;i<rows;i++){
      for (j=0;j<cols;j++){
         mpz_out_str(stdout,10,&Mat[i][j]);
         printf(" ");
      }
      printf("\n");
   }
}
