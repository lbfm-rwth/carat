static void reverse(s)
char s[];
{
  int c,i,j;
  for(i=0, j=strlen(s)-1; i<j; i++, j--)
  {
     c = s[i];
     s[i] = s[j];
     s[j] = c;
  }
}



/**************************************************************************\
@---------------------------------------------------------------------------
@ void itoasc(n, s)
@ int n;
@ char s[];
@
@ changes the integer 'n' to a character 's', for example:  124 --> '124'
@---------------------------------------------------------------------------
@
\**************************************************************************/
void itoasc(n, s)
int n;
char s[];
{
  int i, sign;
  if((sign = n) <0)
    n = -n;
  i=0;
  do
  {
    s[i++] = n%10 + '0';
  }while(( n /= 10) > 0);
  if(sign < 0)
    s[i++] = '-';
  s[i] = '\0';
  reverse(s);
}    
